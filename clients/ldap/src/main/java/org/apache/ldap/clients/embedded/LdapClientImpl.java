    public void connectToServer () throws UnknownHostException, IOException
    {
        InetAddress l_address = InetAddress.getByName( m_host )  ;
        m_socket = new Socket( l_address, m_port ) ;
        m_socket.setKeepAlive( true ) ;
    }

    public boolean isConnectedToServer()
    {
        boolean isConnected = false ;
        if ( m_socket != null )
        {
            isConnected = m_socket.isConnected() ;
        }
        return isConnected ;
    }
    
    public void closeServerConnection () throws IOException
    {
        m_socket.close() ;
    }

    public LdapClientResponse ldapBind() throws IOException
    {
        if ( !isConnectedToServer() )
        {
            connectToServer() ;
        }
        
        // Setup the bind request
        BindRequestImpl l_request = new BindRequestImpl( id ) ;
        l_request.setName( m_bindDn ) ;
        l_request.setSimple( true ) ;
        l_request.setCredentials( m_password.getBytes() ) ;
        l_request.setVersion3( m_isVersion3 ) ;
        
        // Setup the ASN1 Enoder and Decoder
        MessageEncoder l_encoder = new MessageEncoder();
        MessageDecoder l_decoder = new MessageDecoder();
 
        // Send encoded request to server
        l_encoder.encodeBlocking( null, m_socket.getOutputStream(), l_request );
        id++;
        
        // Retrieve the response back from server to my last request.
        BindResponse l_bindResponse = 
            ( BindResponse ) l_decoder.decode( null, m_socket.getInputStream() );
        LdapClientResponse l_clientResponse = new LdapClientResponse() ;
        
        l_clientResponse.setResultCode (
            l_bindResponse.getLdapResult().getResultCode().getValue() ) ;
        l_clientResponse.setErrorMessage(
            l_bindResponse.getLdapResult().getErrorMessage() ) ;
        l_clientResponse.setResultCodeString(
            l_bindResponse.getLdapResult().getResultCode().toString() ) ;
                                 
        return l_clientResponse ;
    }
    
