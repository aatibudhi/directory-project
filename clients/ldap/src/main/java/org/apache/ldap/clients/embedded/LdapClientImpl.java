    public LdapClientResponse ldapAdd ( StringBuffer a_entry )
       throws IOException
    {
        // Setup the ASN1 Enoder and Decoder
        MessageEncoder l_encoder = new MessageEncoder();
        MessageDecoder l_decoder = new MessageDecoder();
        
        AddRequest l_request = new AddRequestImpl( id )  ;
        id++;
        
        try
        {
            // ------------------------------------------
            // We need to change this to lockableAttribute
            // but that impl needs some work first
            //-------------------------------------------
            String l_dn = new String () ;
            LdifParser l_parser = new LdifParserImpl () ;
            Attributes l_attributes = new BasicAttributes () ;

            l_parser.parse( l_attributes, a_entry.toString() ) ;
            Attribute l_attr = l_attributes.get( "dn" ) ;
            if ( l_attr == null )
            {
                return null;
            }
            l_dn = ( String ) l_attr.get() ;
            l_request.setEntry( l_dn ) ;
            l_attributes.remove( "dn" ) ;
            l_request.setAttributes( l_attributes ) ;
        }
        catch ( NamingException ne )
        {
            System.out.println( "Error parsing file " + ne.toString() ) ;
            System.exit( -1 ) ;
        }
        l_encoder.encodeBlocking( null, m_socket.getOutputStream(), l_request ) ;
        AddResponse l_addResponse =
            ( AddResponse ) l_decoder.decode( null, m_socket.getInputStream() ) ;
        LdapClientResponse l_clientResponse = new LdapClientResponse() ;
    
        l_clientResponse.setResultCode (
            l_addResponse.getLdapResult().getResultCode().getValue() ) ;
        l_clientResponse.setErrorMessage(
            l_addResponse.getLdapResult().getErrorMessage() ) ;
        l_clientResponse.setResultCodeString(
            l_addResponse.getLdapResult().getResultCode().toString() ) ;
        l_clientResponse.setDn( l_request.getEntry() ) ;
        
        return l_clientResponse ;
    }
    
   
    public LdapClientResponse ldapModify ( StringBuffer a_entry )
        throws IOException 
    {
        LdapClientResponse l_clientResponse = new LdapClientResponse () ;
        
        // Setup the ASN1 Enoder and Decoder
        MessageEncoder l_encoder = new MessageEncoder();
        MessageDecoder l_decoder = new MessageDecoder();
        
        ModifyRequest l_request = new ModifyRequestImpl( id )  ;
        id++;

        try
        {
          // ------------------------------------------
          // We need to change this to lockableAttribute
          // but that impl needs some work first
          //-------------------------------------------
          LdifParser l_parser = new LdifParserImpl () ;
          LdifEntry l_ldifEntry = null ;
          l_ldifEntry = l_parser.parse( a_entry.toString() ) ;

          l_request.setName( l_ldifEntry.getDn() ) ;
          LinkedList l_items = l_ldifEntry.getModificationItems() ;
          for ( int l_index = 0; l_index < l_items.size(); l_index ++ )
          {
            l_request.addModification(
              ( ModificationItem ) l_items.get( l_index ) ) ;
          }
        }
        catch ( NamingException ne )
        {
          System.out.println( "Error parsing file " + ne.toString() ) ;
          System.exit( -1 ) ;
        }
        l_encoder.encodeBlocking( null, m_socket.getOutputStream(), l_request ) ;

        ModifyResponse l_modifyResponse =
          ( ModifyResponse ) l_decoder.decode( null, m_socket.getInputStream() ) ;

        l_clientResponse.setResultCode (
            l_modifyResponse.getLdapResult().getResultCode().getValue() ) ;
        l_clientResponse.setErrorMessage(
            l_modifyResponse.getLdapResult().getErrorMessage() ) ;
        l_clientResponse.setResultCodeString(
            l_modifyResponse.getLdapResult().getResultCode().toString() ) ;
        l_clientResponse.setDn( l_request.getName() ) ;
        return l_clientResponse ;    
    }

    
